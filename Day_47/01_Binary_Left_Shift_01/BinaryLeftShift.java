
public class BinaryLeftShift {

    public static void main(String[] args) {
        System.out.println(5 << 2); // Left shift 5 by 2 positions
        // This is equivalent to multiplying 5 by 2^2 (4), resulting in 20
        System.out.println(6 << 3); // Left shift 6 by 3 positions
        // This is equivalent to multiplying 6 by 2^3 (8), resulting in 48
    }
}
