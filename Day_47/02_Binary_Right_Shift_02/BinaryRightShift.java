
public class BinaryRightShift {

    public static void main(String[] args) {
        System.out.println(6 >> 1); // Right shift 6 by 1 position
        // This is equivalent to dividing 6 by 2^1 (2), resulting in 3
        System.out.println(6 >> 3); // Right shift 6 by 3 positions
        // This is equivalent to dividing 6 by 2^3 (8), resulting in 0
    }
}
