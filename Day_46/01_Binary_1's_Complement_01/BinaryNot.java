
public class BinaryNot {

    public static void main(String[] args) {
        // Print the 1's complement of 0
        System.out.println((~0)); // This will print -1, which is the 1's complement of 0

        // Print the 1's complement of 5
        System.out.println((~5)); // This will print -6, which is the 1's complement of 5
    }
}
