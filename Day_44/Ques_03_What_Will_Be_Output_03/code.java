// Question 3 : What will be the output of the following code?
// Ans : ApnaCoege

public class code {

    public static void main(String[] args) {
        String str = "ApnaCollege".replace("l", "");
        System.out.println(str); // ApnaCoege
    }
}

// Note: The code replaces all occurrences of the letter 'l' in the string "ApnaCollege" with an empty string, effectively removing them. The output will be "ApnaCoege".

